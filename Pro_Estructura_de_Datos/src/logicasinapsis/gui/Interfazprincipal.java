    private boolean esZonaAislada(String idNodo) {
        if (ultimasZonasAisladas == null) return false;
        for (String id : ultimasZonasAisladas) {
            if (id.equals(idNodo)) return true;
        }
        return false;
    }
    private void SimularlesioncerebralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimularlesioncerebralActionPerformed
        // TODO add your handling code here:
        if (redNeuronal == null || redNeuronal.getCantidadNodos() == 0) {
            JOptionPane.showMessageDialog(this, "Debe cargar la red neuronal primero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String idEliminar = JOptionPane.showInputDialog(this, "Ingrese el ID de la neurona a eliminar (Simular Lesión Focal):");
        if (idEliminar == null || idEliminar.trim().isEmpty()) return;
        idEliminar = idEliminar.trim().toUpperCase();

        // 1. Verificar si la neurona realmente existe antes de proceder
        if (redNeuronal.buscarNeurona(idEliminar) == null) {
            JOptionPane.showMessageDialog(this, "La neurona '" + idEliminar + "' no existe en la red.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Ejecutar la eliminación física en tu estructura de Grafo
        // (Esto remueve la neurona y todas las sinapsis que entraban o salían de ella)
        redNeuronal.eliminarNeurona(idEliminar);

        JOptionPane.showMessageDialog(this, 
                "¡Lesión Cerebral Simulada!\n\n" +
                "La neurona '" + idEliminar + "' y todas sus conexiones asociadas han sido destruidas.", 
                "Simulación de Lesión", JOptionPane.WARNING_MESSAGE);

        // ========================================================
        // 3. ¡SOLUCIÓN DE PERSISTENCIA! OFRECER GUARDAR CAMBIOS
        // ========================================================
        int respuesta = JOptionPane.showConfirmDialog(this, 
                "¿Desea guardar y exportar el estado actual de la red modificada (con la neurona eliminada) en el CSV?", 
                "Guardar Persistencia tras Lesión", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) {
            guardarDatosEnArchivoCSV(); // Llama a la función que ya creamos con el JFileChooser
        }
        // ========================================================

        // 4. Refrescar el mapa visual de GraphStream para que la neurona desaparezca de la pantalla
        sincronizarGrafoVisual();
    }//GEN-LAST:event_SimularlesioncerebralActionPerformed

