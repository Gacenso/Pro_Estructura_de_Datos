    private void ImplantarNuevaNeuronaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImplantarNuevaNeuronaActionPerformed
        // TODO add your handling code here:
       if (redNeuronal == null) {
        JOptionPane.showMessageDialog(this, "Debe cargar la red neuronal primero.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 1. Solicitar el ID de la nueva neurona a implantar
    String nuevoId = JOptionPane.showInputDialog(this, "Ingrese el ID para la nueva neurona (Ej: N99):");
    if (nuevoId == null || nuevoId.trim().isEmpty()) return;
    nuevoId = nuevoId.trim().toUpperCase();

    // Validar que no exista ya en la red en memoria
    if (redNeuronal.buscarNeurona(nuevoId) != null) {
        JOptionPane.showMessageDialog(this, "La neurona '" + nuevoId + "' ya existe en la red.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 2. Insertar la neurona físicamente en el grafo
    redNeuronal.agregarNeurona(new logicasinapsis.modelo.Neurona(nuevoId));
    
    // Contador para informar cuántas sinapsis se han creado
    int conexionesCreadas = 0;
    boolean seguirConectando = true;

    // 3. BUCLE INTERACTIVO: Permite conectar a 2 o más neuronas consecutivamente
    while (seguirConectando) {
        String mensajePrompt = (conexionesCreadas == 0) 
            ? "Ingrese el ID de la PRIMERA neurona destino existente para conectar (origen: " + nuevoId + "):"
            : "Lleva " + conexionesCreadas + " conexión(es).\nIngrese OTRO ID de neurona destino para conectar de inmediato\n(O pulse 'Cancelar' para finalizar):";

        String idDestino = JOptionPane.showInputDialog(this, mensajePrompt);
        
        // Si el usuario presiona Cancelar o deja vacío, se rompe el ciclo
        if (idDestino == null || idDestino.trim().isEmpty()) {
            seguirConectando = false;
            break;
        }
        
        idDestino = idDestino.trim().toUpperCase();
        
        // Validar si el destino ingresado existe en el grafo
        if (redNeuronal.buscarNeurona(idDestino) == null) {
            JOptionPane.showMessageDialog(this, "La neurona destino '" + idDestino + "' no existe.\nIntente con un ID válido.", "Neurona No Encontrada", JOptionPane.WARNING_MESSAGE);
            continue; // Regresa al inicio del bucle para pedir otra sin detener el proceso
        }

        try {
            // Solicitar distancia (d)
            String inputDist = JOptionPane.showInputDialog(this, "Conectando " + nuevoId + " -> " + idDestino + "\nIngrese la distancia de la sinapsis (double):", "2.5");
            if (inputDist == null) continue; // Si cancela este paso, salta a la siguiente iteración
            double distancia = Double.parseDouble(inputDist.trim());

            // Solicitar Neurotransmisor (ID)
            String inputNT = JOptionPane.showInputDialog(this, "Ingrese el ID del neurotransmisor (GLU, DA, GABA, ACH):", "GLU");
            if (inputNT == null) continue;
            String idNT = inputNT.trim().toUpperCase();

            // Solicitar coeficiente k
            String inputK = JOptionPane.showInputDialog(this, "Ingrese la eficiencia sináptica k (0.1 a 1.0):", "1.0");
            if (inputK == null) continue;
            double k = Double.parseDouble(inputK.trim());
            if (k < 0.1 || k > 1.0) k = 1.0; // Control regulatorio

            // CORRECCIÓN HISTÓRICA: Se conecta EN UN SOLO SENTIDO (Unidireccional)
            // Estímulo viaja estrictamente desde la Nueva Neurona hacia la Neurona Destino
            redNeuronal.conectar(nuevoId, idDestino, distancia, idNT, k);
            
            conexionesCreadas++;
            
            // Preguntar explícitamente si desea añadir otra conexión más
            int opcion = JOptionPane.showConfirmDialog(this, 
                    "¿Desea agregar otra conexión saliente adicional para la neurona '" + nuevoId + "'?", 
                    "Agregar múltiples enlaces", JOptionPane.YES_NO_OPTION);
            
            if (opcion != JOptionPane.YES_OPTION) {
                seguirConectando = false;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Formato numérico inválido. No se registró esta conexión.", "Error de Datos", JOptionPane.ERROR_MESSAGE);
        }
    }

    // 4. Mensaje de cierre de operación clínica
    if (conexionesCreadas > 0) {
        JOptionPane.showMessageDialog(this, "Implantación exitosa.\nSe integró la neurona '" + nuevoId + "' con " + conexionesCreadas + " sinapsis dirigidas.");
    } else {
        JOptionPane.showMessageDialog(this, "Neurona '" + nuevoId + "' implantada de forma aislada (Sin enlaces de salida).", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }

    // 5. Forzar la actualización inmediata en el mapa visual de GraphStream
    sincronizarGrafoVisual();
    if (conexionesCreadas > 0) {
        JOptionPane.showMessageDialog(this, "Implantación exitosa.\nSe integró la neurona '" + nuevoId + "' con " + conexionesCreadas + " sinapsis dirigidas.");
    } else {
        JOptionPane.showMessageDialog(this, "Neurona '" + nuevoId + "' implantada de forma aislada (Sin enlaces de salida).", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }

    // ==========================================
    // ¡AÑADE ESTO AQUÍ PARA CUMPLIR LA RÚBRICA!
    // ==========================================
    int respuestaSave = JOptionPane.showConfirmDialog(this, 
            "¿Desea guardar y exportar la red con la nueva neurona implantada en el archivo CSV?", 
            "Guardar Cambios", JOptionPane.YES_NO_OPTION);

    if (respuestaSave == JOptionPane.YES_OPTION) {
        guardarDatosEnArchivoCSV(); // Llama a tu función de guardado
    }
    // ==========================================

    // 5. Forzar la actualización inmediata en el mapa visual de GraphStream
    sincronizarGrafoVisual();
    }//GEN-LAST:event_ImplantarNuevaNeuronaActionPerformed
    private void guardarDatosEnArchivoCSV() {
        if (redNeuronal == null || redNeuronal.getCantidadNodos() == 0) {
            JOptionPane.showMessageDialog(this, "No hay datos cargados para guardar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JFileChooser selectorArchivo = new JFileChooser();
        selectorArchivo.setDialogTitle("Guardar Estado Clínico de la Red Neuronal (.csv)");
        
        // Filtro para forzar extensiones .csv
        selectorArchivo.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Archivos CSV (*.csv)", "csv"));
        int seleccion = selectorArchivo.showSaveDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = selectorArchivo.getSelectedFile();
            String ruta = archivoSeleccionado.getAbsolutePath();

            // Si el usuario no escribió la extensión, se la anexamos automáticamente
            if (!ruta.toLowerCase().endsWith(".csv")) {
                ruta += ".csv";
            }

            // Ejecutamos la persistencia física
            lector.guardarRedCSV(ruta, redNeuronal);

            JOptionPane.showMessageDialog(this, 
                    "¡Datos sincronizados y guardados correctamente!\nArchivo: " + archivoSeleccionado.getName(), 
                    "Persistencia Exitosa", JOptionPane.INFORMATION_MESSAGE);
        }
    }
