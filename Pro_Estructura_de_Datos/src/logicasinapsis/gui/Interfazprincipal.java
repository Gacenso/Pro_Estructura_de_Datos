    private void sincronizarGrafoVisual() {
        try {
            // 1. Limpiar el grafo visual previo para redibujarlo de cero
            graphVisual.clear();
            
            // 2. HOJA DE ESTILOS ADAPTADA (Perfecta para GS 2.0 en Swing)
            graphVisual.setAttribute("ui.stylesheet", 
                "node { " +
                "   fill-color: #CCCCCC; " + 
                "   size: 24px; " + 
                "   text-size: 13px; " +          
                "   text-color: black; " + 
                "   text-alignment: center; " + 
                "} " +
                "edge { " +
                "   text-size: 10px; " +           
                "   text-color: #333333; " + 
                "   text-background-mode: plain; " + 
                "   text-background-color: white; " + 
                "   arrow-size: 8px, 5px; " +     
                "   text-offset: 0px, 3px; " + 
                "}");
            
            // 3. Añadir Nodos (Neuronas) desde el Grafo interno en memoria
            for (int i = 0; i < redNeuronal.getCantidadNodos(); i++) {
                logicasinapsis.modelo.Neurona n = redNeuronal.getNeuronaEnPosicion(i);
                if (n != null) {
                    org.graphstream.graph.Node nodoVisual = graphVisual.addNode(n.getId());
                    nodoVisual.setAttribute("ui.label", n.getId());
                    
                    // COMPROBACIÓN PERSISTENTE DEL BFS: Si la neurona está aislada, se fuerza a color ROJO
                    if (esZonaAislada(n.getId())) {
                        nodoVisual.setAttribute("ui.style", "fill-color: red; text-color: white;");
                    }
                }
            }
            
            // 4. Añadir Aristas (Sinapsis) con etiquetas y colores dinámicos
            for (int i = 0; i < redNeuronal.getCantidadNodos(); i++) {
                logicasinapsis.modelo.Neurona origen = redNeuronal.getNeuronaEnPosicion(i);
                if (origen == null) continue;
                
                logicasinapsis.estructuras.Grafo.ListaSinapsis.NodoSinapsis actual = 
                        redNeuronal.getPrimerNodoSinapsis(i);
                
                while (actual != null) {
                    String idDestino = actual.sinapsis.getDestino().getId();
                    String idArista = origen.getId() + "-" + idDestino;
                    
                    if (graphVisual.getEdge(idArista) == null) {
                        org.graphstream.graph.Edge aristaVisual = graphVisual.addEdge(idArista, origen.getId(), idDestino, true);
                        
                        double dist = actual.sinapsis.getDistancia(); 
                        String nt = actual.sinapsis.getIdNeurotransmisor(); 
                        double k = actual.sinapsis.getK(); 
                        
                        String etiqueta = String.format("d:%.1f (%s) k:%.1f", dist, nt, k);
                        aristaVisual.setAttribute("ui.label", etiqueta);
                        
                        // --- REQUERIMIENTO VISUAL DE DIJKSTRA: COLOR AZUL PARA RUTA ÓPTIMA ---
                        if (esAristaDeRutaOptima(origen.getId(), idDestino)) {
                            // Se pinta de un color Azul Eléctrico / Cian brillante y se hace más gruesa
                            aristaVisual.setAttribute("ui.style", "fill-color: #00CCFF; size: 4px; text-color: #0044AA; text-size: 11px;");
                        } else {
                            // Si no forma parte de la ruta corta, mantiene su color reglamentario por Neurotransmisor
                            String color = "#888888"; 
                            if (nt.equalsIgnoreCase("GLU") || nt.equalsIgnoreCase("ACH")) {
                                color = "blue";
                            } else if (nt.equalsIgnoreCase("DA")) {
                                color = "green";
                            } else if (nt.equalsIgnoreCase("GABA")) {
                                color = "red";
                            }
                            aristaVisual.setAttribute("ui.style", "fill-color: " + color + "; size: 1px;");
                        }
                    }
                    actual = actual.siguiente;
                }
            }
            
            // 5. Configurar el motor de renderizado oficial para GraphStream 2.0
            org.graphstream.ui.view.Viewer viewer = new org.graphstream.ui.swing_viewer.SwingViewer(graphVisual, Viewer.ThreadingModel.GRAPH_IN_ANOTHER_THREAD);
            
            // 6. Obtener el panel de la vista integrada en Swing
            ViewPanel viewPanel = (ViewPanel) viewer.addDefaultView(false); 
            
            // SOLUCIÓN AL GRÁFICO CORTADO: Ajustar dinámicamente al tamaño actual disponible de tu contenedor
            viewPanel.setPreferredSize(jScrollPane1.getSize());
            viewPanel.setSize(jScrollPane1.getSize()); 
            
            viewPanel.getCamera().setAutoFitView(false);
            
            // 7. Configuración del motor de fuerzas de distribución física
            graphVisual.setAttribute("layout.force", 0.15);       // Fuerza elástica calibrada para que no se expandan tanto
            graphVisual.setAttribute("layout.quality", 4);         // Máxima precisión de dibujado posicional
            
            // Activar el Layout automático continuo
            viewer.enableAutoLayout();
            
            // Hilo temporizado muy corto para forzar un encuadre centrado perfecto en pantalla
            new javax.swing.Timer(250, e -> {
                if (viewPanel.getCamera() != null) {
                    viewPanel.getCamera().resetView(); // Limpia zooms residuales erróneos
                    viewPanel.getCamera().setAutoFitView(true); // Fuerza el encuadre exacto al tamaño del JScrollPane
                }
            }).start();
            
            // Evitar cierres inesperados de hilos en cascada de Java Swing
            viewer.setCloseFramePolicy(org.graphstream.ui.view.Viewer.CloseFramePolicy.CLOSE_VIEWER);
            
            // 8. Ocultar las barras de scroll fijas del JScrollPane para que se vea más limpio el lienzo interactivo
            jScrollPane1.setHorizontalScrollBarPolicy(javax.swing.ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER);
            jScrollPane1.setVerticalScrollBarPolicy(javax.swing.ScrollPaneConstants.VERTICAL_SCROLLBAR_NEVER);
            
            // 9. Incrustar el panel calibrado en tu ventana y refrescar la UI de Java
            jScrollPane1.setViewportView(viewPanel);
            jScrollPane1.revalidate();
            jScrollPane1.repaint();
            this.revalidate();
            this.repaint();
            
        } catch (Exception e) {
            System.err.println("Error crítico al renderizar el mapa visual: " + e.getMessage());
            e.printStackTrace(); 
        }
    }
    private boolean esAristaDeRutaOptima(String idOrigen, String idDestino) {
        if (ultimaRutaDijkstra == null || ultimaRutaDijkstra.length < 2) return false;
        for (int i = 0; i < ultimaRutaDijkstra.length - 1; i++) {
            if (ultimaRutaDijkstra[i].equals(idOrigen) && ultimaRutaDijkstra[i+1].equals(idDestino)) {
                return true;
            }
        }
        return false;
    }
